    public void palabra() {
        String frase = txtFra.getText();
        if (capitalizar(frase) >= 5) {
            String fraseCapitalizada = capitalize(frase);
            lblRe.setText("La frase capitalizada es : " + fraseCapitalizada);
        } else {
            lblRe.setText("La frase debe contener al menos 5 palabras. "
                    + "Ingresa nuevamente.");
        }
    }
   
    public static int capitalizar (String frase) {
        if (frase == null || frase.isEmpty()) {
            return 0;
        }
        String[] words = frase.trim().split("\\s+");
        return words.length;
    }

    public static String capitalize(String frase) {
        if (frase == null || frase.isEmpty()) {
            return "";
        }
        String[] words = frase.trim().split("\\s+");
        StringBuilder sb = new StringBuilder();
        for (String word : words) {
            char firstLetter = Character.toUpperCase(word.charAt(0));
            sb.append(firstLetter).append(word.substring(1)).append(" ");
        }
        return sb.toString().trim();
    }
    
