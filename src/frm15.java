      public boolean isNumber(String numero) {
        try {
            int Number = Integer.parseInt(numero);
            return true;
        } catch (NumberFormatException NFE) {
            JOptionPane.showMessageDialog(this,
                    "El texto " + numero + " no es un numero valido",
                    "Numero Invalido", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
      public void numero() {
        if (isNumber(txtNum.getText())) {
            int numero = Integer.parseInt(txtNum.getText());
            if (numero < 0 || numero > 99) {
                JOptionPane.showMessageDialog(this, 
                        "Numero Fuera de Rango", "Numero Incorrecto", JOptionPane.ERROR_MESSAGE);
            } else {
                String numeroEscrito = converNumEscrito(numero);
                lblRe.setText(numero + " en palabras es: " + numeroEscrito);
            }
        }
    }
      public static String converNumEscrito(int numero) {
        String[] unidades = {"cero", "uno", "dos", "tres", "cuatro", "cinco", "seis", "siete", "ocho", "nueve"};
        String[] especiales = {"diez", "once", "doce", "trece", "catorce", "quince", "diecis√©is", "diecisiete", "dieciocho", "diecinueve"};
        String[] decenas = {"", "", "veinte", "treinta", "cuarenta", "cincuenta", "sesenta", "setenta", "ochenta", "noventa"};
        if (numero < 10) {
            return unidades[numero];
        } else if (numero >= 10 && numero < 20) {
            return especiales[numero - 10];
        } else {
            int decena = numero / 10;
            int unidad = numero % 10;
            String resultado = decenas[decena];
            if (unidad > 0) {
                resultado += " y " + unidades[unidad];
            }
            return resultado;
        }
    }

